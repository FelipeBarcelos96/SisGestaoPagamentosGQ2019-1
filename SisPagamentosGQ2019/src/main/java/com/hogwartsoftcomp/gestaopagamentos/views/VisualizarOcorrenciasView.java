    private void visualizarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_visualizarButtonActionPerformed
        if(codigoTextField.getText() != ""){
        IService<Pagamento> pagamentoService = new GenericService<Pagamento>(Pagamento.class);
        List<Pagamento> pag = pagamentoService.getAll();
        for (Pagamento pagamento: pag) {
            if (pagamento.getId() == Integer.parseInt(codigoTextField.getText())){
              try {
                descricaoTextArea.setText(pagamento.getDescricao());
                solicitanteLabel.setText(pagamento.getSolicitante().getNome());
                valorLabel.setText(Double.toString(pagamento.getValor()));
                dtVencimentoLabel.setText(ValidaData.getInstance().converteData(pagamento.getDataVencimento()));
                if(pagamento.getDataPagamento()!= null){
                dtPagamentoLabel.setText(ValidaData.getInstance().converteData(pagamento.getDataPagamento()));
                }
            } catch (ParseException ex) {
            Logger.getLogger(PagamentosView.class.getName()).log(Level.SEVERE, null, ex);
        }
            }
        }
        clearTable();
            try {
                populateTable();
            } catch (ParseException ex) {
                Logger.getLogger(VisualizarOcorrenciasView.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_visualizarButtonActionPerformed

