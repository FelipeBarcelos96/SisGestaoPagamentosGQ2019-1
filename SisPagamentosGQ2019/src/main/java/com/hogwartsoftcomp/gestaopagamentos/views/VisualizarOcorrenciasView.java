    private void populateTable() throws ParseException {

        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();

        IService<Pagamento> pagamentoService = new GenericService<Pagamento>(Pagamento.class);

        List<Pagamento> pagamentos = pagamentoService.getAll();
        if (pagamentos != null) {
            for (Pagamento pagamento : pagamentos) {
                if(pagamento.getId() == Integer.parseInt(codigoTextField.getText())){
                    List<OcorrenciasPagamento> ocorrencias = pagamento.getOcorrencias();
                    for (OcorrenciasPagamento ocorrencia : ocorrencias) {
                        model.addRow(new Object[]{ValidaData.getInstance().converteData(ocorrencia.getDataDaOcorrencia()), ocorrencia.getDescricao(), ocorrencia.getSolicitante()});
                    }
                }
                
            }
        }
    }
    
    private void clearTable() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        for (int i = jTable1.getRowCount() - 1; i >= 0; i--) {
            model.removeRow(i);
        }
    }
    
