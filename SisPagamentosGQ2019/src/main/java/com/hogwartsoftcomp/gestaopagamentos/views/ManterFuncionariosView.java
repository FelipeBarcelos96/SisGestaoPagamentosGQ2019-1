    private void buscarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscarButtonActionPerformed
        
        IService<Funcionario> funcionarioService = new GenericService<Funcionario>(Funcionario.class);
        List<Funcionario> funcs = funcionarioService.getAll();
         if (funcs != null) {
            for (Funcionario funcionario : funcs) {
                if(nomeTextField.getText() != null && cargoTextField.getText() == null){
                 if(funcionario.getNome() == nomeTextField.getText()){
                    cargoTextField.setText(funcionario.getCargo());
                 }
                }
                if(nomeTextField.getText() == null && cargoTextField.getText() != null){
                 if(funcionario.getCargo() == cargoTextField.getText()){
                    nomeTextField.setText(funcionario.getNome());
                 }
                } 
            }
         }
    }//GEN-LAST:event_buscarButtonActionPerformed

    private void excluirButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_excluirButtonActionPerformed
        funcDao = new FuncionarioDao(Funcionario.class, HibernateUtil.getSessionFactory());
        IService<Funcionario> funcionarioService = new GenericService<Funcionario>(Funcionario.class);
        List<Funcionario> funcs = funcionarioService.getAll();
        if (funcs != null) {
            for (Funcionario funcionario : funcs) {
              if(nomeTextField.getText() == funcionario.getNome() && cargoTextField.getText() == funcionario.getCargo()){
                  funcDao.delete(funcionario);
              } 
            }
        }    
    }//GEN-LAST:event_excluirButtonActionPerformed

